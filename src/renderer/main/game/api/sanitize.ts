const replaceMap: { [key: string]: string } = {
  "@": "＠",
  "#": "＃",
  $: "＄",
  "%": "％",
  "&": "＆",
  "=": "＝",
  "+": "＋",
  "/": "⁄",
  "\\": "＼",
  ";": ";",
  ":": "˸",
  ",": "‚",
  "?": "？",
  "!": "ǃ",
  '"': "＂",
  "<": "≺",
  ">": "≻",
  ".": "․",
  "^": "＾",
  "{": "｛",
  "}": "｝",
  "[": "［",
  "]": "］",
  "(": "（",
  ")": "）",
  "|": "｜",
  "*": "∗",
};

export function replacePunctuation(text: string) {
  return text.replace(/[@#$%&=+/\\;:,?!"<>.^{}[\]()|*]/g, (c) => replaceMap[c]);
}
